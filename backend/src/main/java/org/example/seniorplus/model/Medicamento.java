package org.example.seniorplus.model;

import java.time.LocalTime;
import java.util.List;

// Plain Java model (DTO) â€” not a JPA entity. Removed JPA annotations to avoid duplicate entity mappings.
public class Medicamento {
    private Long id;

    private String nome;
    private String dosagem;
    private String frequencia;

    private List<LocalTime> horarios;

    private Idoso idoso;

    // Getters e Setters
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getDosagem() {
        return dosagem;
    }

    public void setDosagem(String dosagem) {
        this.dosagem = dosagem;
    }

    public String getFrequencia() {
        return frequencia;
    }

    public void setFrequencia(String frequencia) {
        this.frequencia = frequencia;
    }

    public List<LocalTime> getHorarios() {
        return horarios;
    }

    public void setHorarios(List<LocalTime> horarios) {
        this.horarios = horarios;
    }

    public Idoso getIdoso() {
        return idoso;
    }

    public void setIdoso(Idoso idoso) {
        this.idoso = idoso;
    }
}